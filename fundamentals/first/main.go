// todo arquivo precisa ter um pacote, com exceção do main o nome deve ser o mesmo do diretório
// main é o package principal, o que indica que o arquivo é executável
// todos os arquivos que estão no mesmo diretório devem ter o mesmo package
// tudo que está dentro do mesmo package pode ser acessado por outros arquivos do mesmo package
package main

// função principal que será executada quando o programa for executado
func main() {
	// println é uma função que imprime uma linha de texto no console
	println("Hello, World!")
}